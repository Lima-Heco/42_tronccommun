#include "libft.h"

char	*ft_strjoin(char *s1, char *s2, int fd)
{
	char	*str;
	size_t	i;
	size_t	j;

	str = (char *)malloc(sizeof(*s1) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
		str[j++] = s1[i++];
	i = 0;
	while (s2[i])
		str[j++] = s2[i++];
	str[j] = 0;
	if (fd == 1 || fd == 3)
		free(s1);
	if (fd == 2 || fd == 3)
		free(s2);
	return (str);
}
